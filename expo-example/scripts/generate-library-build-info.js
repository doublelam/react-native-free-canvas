#!/usr/bin/env node
/* eslint-disable @typescript-eslint/no-require-imports */
/**
 * Writes version + git identity for the linked react-native-free-canvas source
 * so the demo UI can show whether Metro is on the expected local tree.
 */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const expoExampleRoot = path.resolve(__dirname, '..');
const libraryRoot = path.resolve(expoExampleRoot, '..');
const outDir = path.join(expoExampleRoot, 'generated');
const outFile = path.join(outDir, 'library-build-info.ts');

function runGit(command) {
  try {
    return execSync(command, {
      cwd: libraryRoot,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return '';
  }
}

const packageJson = JSON.parse(
  fs.readFileSync(path.join(libraryRoot, 'package.json'), 'utf8'),
);
const version = String(packageJson.version ?? '0.0.0');
const gitSha = runGit('git rev-parse --short HEAD') || 'unknown';
const dirty = Boolean(runGit('git status --porcelain'));

const contents = `/* eslint-disable */
// Auto-generated by scripts/generate-library-build-info.js — do not edit.
export const LIBRARY_NAME = 'react-native-free-canvas' as const;
export const LIBRARY_VERSION = ${JSON.stringify(version)} as const;
export const LIBRARY_GIT_SHA = ${JSON.stringify(gitSha)} as const;
export const LIBRARY_DIRTY = ${dirty ? 'true' : 'false'} as const;
export const LIBRARY_SOURCE = 'src' as const;
`;

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, contents);

console.log(
  `library-build-info: ${version} · ${gitSha}${dirty ? '*' : ''} (src)`,
);
